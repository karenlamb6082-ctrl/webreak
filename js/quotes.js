const MBTI_TO_GROUP = {
  'INTJ': 'analyst', 'INTP': 'analyst', 'ENTJ': 'analyst', 'ENTP': 'analyst',
  'INFJ': 'diplomat', 'INFP': 'diplomat', 'ENFJ': 'diplomat', 'ENFP': 'diplomat',
  'ISTJ': 'sentinel', 'ISFJ': 'sentinel', 'ESTJ': 'sentinel', 'ESFJ': 'sentinel',
  'ISTP': 'explorer', 'ISFP': 'explorer', 'ESTP': 'explorer', 'ESFP': 'explorer',
};

/**
 * 根据 MBTI 类型获取一句随机备注
 * @param {string|null} mbti - MBTI 类型，如 'INFJ'；为空则使用通用语句
 * @param {string|null} excludeQuote - 需要排除的语句（用于"换一句"功能）
 * @returns {string} 随机选取的备注语句
 */
function getRandomQuote(mbti, excludeQuote) {
  const group = mbti ? MBTI_TO_GROUP[mbti] : null;
  let pool = group ? [...QUOTES[group], ...QUOTES.general] : [...QUOTES.general];
  
  if (excludeQuote) {
    pool = pool.filter(q => q !== excludeQuote);
  }
  
  if (pool.length === 0) {
    pool = [...QUOTES.general];
  }
  
  return pool[Math.floor(Math.random() * pool.length)];
}

